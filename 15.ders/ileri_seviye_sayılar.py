# binary

print(bin(9))

print(bin(19))

print(bin(512))

# hexadecimal
print(hex(10))
print(hex(32))


# fonksiyon abs-mutlak değer

print(abs(-4))

print(abs(4.5))

print(abs(-10))

# round fonksiyonu sayıları yuvarlar

print(round(5.6))
print(round(5.4))
print(round(3.146544545454))

print(round(2.12345,4))
print(round(2.12342,4))

# max ve min fonksiyonu

print(max([1,2,3,4,5]))

print(max(1,2,3))

print(min(55,22,15,9,454))

# sum fonksiyonu - toplama fonksiyonu

print(sum([1,2]))
print(sum(range(0,100)))

# pow fonksiyonu - power - üssünü alan fonksiyon
print(pow(2,5))
print(pow(2,2))
print(pow(5,2))

print(pow(4,0.5))
print(pow(36,0.5))




