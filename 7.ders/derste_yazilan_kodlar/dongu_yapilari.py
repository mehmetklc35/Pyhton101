
# Döngü Yapılarını Kullanma
# Şimdiye kadar yazdığımız programlarda yazdığımız programlar 
# bir defa çalışıyor ve sona eriyordu. Ancak biz çoğu zaman 
# programlarımızın belli koşullarda çalışmasını sürekli devam 
# ettirmesini ve işlemlerini tekrar etmesini isteriz. İşte bunları 
# yapmamızı sağlayan yapılara döngü diyoruz.

# Döngüler bütün programlama dillerinde bulunan ve belli koşullarda 
# işlemlerini sürekli tekrar eden yapılardır. İsterseniz gerçek hayattaki
# programlara bakarak döngü mantığını anlamaya çalışalım.

# Örneğin , siz ATM makinesine gidip kartınızı yerleştiriyorsunuz ve
# program başlıyor. Para Çekme, Para Yatırma , Vergileri Ödeme gibi 
# işlemleri tekrar tekrar gerçekleştiriyorsunuz. Programın sona ermesi 
# ise Kart İade seçeneği ile gerçekleşiyor. Yani siz Kart İade tuşuna 
# basmadığınız sürece ATM makinesi çalışmaya devam ediyor. Buna bakarak 
# ,aslında ATM makinesi döngü yapılarını kullanıyor diyebiliriz.

# Başka bir örnek düşünelim. Örneğin siz bir siteye login olma işlemi 
# gerçekleştiriyorsunuz. Biz kullanıcı adı ve parolayı yanlış 
# girdiğimiz sürece program sürekli bize kullanıcı adı ve parola soruyor. 
# Programın sona ermesi ise biz kullanıcı adı ve parolayı doğru girdiğimizde 
# gerçekleşiyor. Yine burada da siz döngü yapılarının kullanıldığını düşünebilirsiniz.

# Biz de artık bu bölümle birlikte Pythondaki while ve for 
# döngülerini kullanarak programlarımızı daha efektif bir şekilde yazabileceğiz.